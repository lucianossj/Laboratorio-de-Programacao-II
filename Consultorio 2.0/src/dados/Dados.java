package dados;

import java.util.Date;

public class Dados {

	private String 	dadoString;
	private int		dadoInt;
	private Date 	dadoDate;
	
	public String getDadoString() {
		return dadoString;
	}
	public void setDadoString(String dadoString) {
		this.dadoString = dadoString;
	}
	public int getDadoInt() {
		return dadoInt;
	}
	public void setDadoInt(int dadoInt) {
		this.dadoInt = dadoInt;
	}
	public Date getDadoDate() {
		return dadoDate;
	}
	public void setDadoDate(Date dadoDate) {
		this.dadoDate = dadoDate;
	}
	

	
	
}
